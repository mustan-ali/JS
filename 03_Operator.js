let a = 10
let b = 3

//Arithmetic Operators
console.log(a + b)
console.log(a - b)
console.log(a * b)
console.log(a / b)
console.log(a % b)
console.log(a ** b)     // a^b

//Postfix and Prefix Increment/Decrement Operators
console.log(a++)        // Print a then increment a
console.log(a--)        // Print a then decrement a

console.log(++a)        // Increment a then print a
console.log(--a)        // Decrement a then print a
console.log(" ")

//Assignment Operators
console.log(c = 10)       // c = 10
console.log(c += 1)       // c = c + 5
console.log(c -= 2)       // c = c - 5
console.log(c *= 3)       // c = c * 5
console.log(c /= 4)       // c = c / 5
console.log(c %= 5)       // c = c % 5
console.log(c **= 6)      // c = c ** 5

let d = 5
let e = '5'

//Comparison Operators
console.log(d == e)     //true
console.log(d === e)    //false
console.log(d != e)     //false
console.log(d !== e)    //true
console.log(d > e)      //false
console.log(d < e)      //false
console.log(d >= e)     //true
console.log(d <= e)     //true


