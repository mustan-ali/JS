let color = 'Red'

//IF Statement
if (color == 'Red') {
    console.log("Stop")
}


//IF-ELSE Statement
if (color == 'Red') {
    console.log("Stop")
}
else {
    console.log("Go")
}


//IF-ELSEIF-ELSE Statement
if (color == 'Red') {
    console.log("Stop")
}
else if (color == 'Yellow') {
    console.log("Ready")
}
else {
    console.log("Go")
}


//Ternary Operator
let message = (color == 'Red') ? "Stop" : "Go"
